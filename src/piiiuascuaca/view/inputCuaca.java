    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtTanggal.getText().isEmpty()){
            this.dispose();
        }else{
        String newDateString = "";
        DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date startDate;
        try {
            startDate = dateFormat.parse(txtTanggal.getText());
            DateFormat dateFormat2 = new SimpleDateFormat("yyyy-MM-dd");
            newDateString = dateFormat2.format(startDate);
        } catch (ParseException e) {
            e.printStackTrace();
        }

        try{
            if(id_cuaca.isEmpty()){
                PreparedStatement st = koneksi.getConnection().prepareStatement(""
                        + "INSERT INTO t_cuaca "
                        + " (`date`, `nama_kota`, `suhu`, `suhu_maks`, `suhu_min`, "
                        + "`kecepatan_angin`, `kelembapan`, `tekanan_udara`) VALUES(?, ?, ?, ?, ?, ?, ?, ?)");
                    st.setString(1, newDateString + " 00:00:00");
                    st.setString(2, txtNamaKota.getText());
                    st.setString(3, txtSuhu.getText());
                    st.setString(4, txtSuhuMaks.getText());
                    st.setString(5, txtSuhuMin.getText());
                    st.setString(6, txtKecepatanAngin.getText());
                    st.setString(7, txtKelembapan.getText());
                    st.setString(8, txtTekananUdara.getText());
                    System.out.println(st);
                    st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil di Input");
                
                txtTanggal.setText("");
                txtNamaKota.setText("");
                txtSuhu.setText("");
                txtSuhuMaks.setText("");
                txtSuhuMin.setText("");
                txtKecepatanAngin.setText("");
                txtKelembapan.setText("");
                txtTekananUdara.setText("");
            }else{
                PreparedStatement st = koneksi.getConnection().prepareStatement(""
                        + "UPDATE t_cuaca "
                        + "SET `date` = ?, `nama_kota` = ? , `suhu` = ?, `suhu_maks` = ?, `suhu_min` = ?,"
                        + "`kecepatan_angin`= ?, `kelembapan`= ?, `tekanan_udara`= ? WHERE `id` = ?");
                    st.setString(1, newDateString + " 00:00:00");
                    st.setString(2, txtNamaKota.getText());
                    st.setString(3, txtSuhu.getText());
                    st.setString(4, txtSuhuMaks.getText());
                    st.setString(5, txtSuhuMin.getText());
                    st.setString(6, txtKecepatanAngin.getText());
                    st.setString(7, txtKelembapan.getText());
                    st.setString(8, txtTekananUdara.getText());
                    st.setString(9, id_cuaca);
                    
                    st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil di Update");
            }
            }catch(SQLException ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
