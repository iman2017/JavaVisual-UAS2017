    public inputCuaca(String id) throws SQLException{
        initComponents();
        id_cuaca = id;
        if(!id_cuaca.isEmpty()){
            Statement st = koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM t_cuaca WHERE id = '"+ id_cuaca +"'");
            while(rs.next()){
                tanggal = rs.getString("date");
                txtNamaKota.setText(rs.getString("nama_kota"));
                txtSuhu.setText(rs.getString("suhu"));
                txtSuhuMaks.setText(rs.getString("suhu_maks"));
                txtSuhuMin.setText(rs.getString("suhu_min"));
                txtKecepatanAngin.setText(rs.getString("kecepatan_angin"));
                txtKelembapan.setText(rs.getString("kelembapan"));
                txtTekananUdara.setText(rs.getString("tekanan_udara"));
            }
        }
        tanggal = (tanggal.substring(8, 10)) + "-" + tanggal.substring(5, 7) + "-"+ tanggal.substring(0, 4);
        txtTanggal.setText(tanggal.substring(0, 10));
    }
